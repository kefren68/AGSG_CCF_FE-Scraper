# Authentication parameters that ScreenScraper embeds in media URLs.
# These must be stripped before caching URLs to disk.
_AUTH_PARAMS = frozenset({"devid", "devpassword", "ssid", "sspassword", "softname"})

def _strip_auth_params(url):
    """Return url with ScreenScraper authentication query parameters removed."""
    parsed = urlparse(url)
    clean  = {k: v[0] for k, v in parse_qs(parsed.query, keep_blank_values=True).items()
              if k not in _AUTH_PARAMS}
    return urlunparse(parsed._replace(query=urlencode(clean)))


# ---------------------------------------------------------------------------
# ROM filename cleaner: strips region/revision tags for fallback searches
# e.g. "Super Mario World (USA) (Rev 1) [!].sfc" → "Super Mario World"
# WHDLoad Amiga format: "FirstSamurai_v2.1_1182.lha" → "FirstSamurai"
# ---------------------------------------------------------------------------
_TAG_RE      = re.compile(r'[\(\[][^\)\]]*[\)\]]')
_WHDLOAD_RE  = re.compile(r'_v\d.*$|_\d{3,}.*$', re.IGNORECASE)  # _v2.1_1182 etc.
_UNDERSCORE_RE = re.compile(r'[_]+')

def strip_rom_tags(filename):
    """Return filename with all (…) and […] tags removed, trimmed."""
    stem = Path(filename).stem
    cleaned = _TAG_RE.sub('', stem).strip(' .-_')
    ext = Path(filename).suffix
    return (cleaned + ext) if cleaned else filename

def fallback_names(filename):
    """
    Generate a sequence of progressively simpler names to try on ScreenScraper.
    Yields names that differ from the original (skip duplicates).
    """
    ext  = Path(filename).suffix
    stem = Path(filename).stem
    seen = {filename.lower()}

    def _try(name):
        n = name.strip(' .-_')
        candidate = (n + ext) if n else None
        if candidate and candidate.lower() not in seen:
            seen.add(candidate.lower())
            return candidate
        return None

    # Step 1: strip (…) and […] tags
    s1 = _TAG_RE.sub('', stem)
    if (c := _try(s1)):
        yield c
        stem = s1.strip(' .-_')   # use as base for further steps

    # Step 2: strip WHDLoad-style _vX.Y_NNNN suffixes
    s2 = _WHDLOAD_RE.sub('', stem)
    if (c := _try(s2)):
        yield c
        stem = s2.strip(' .-_')

    # Step 3: progressively strip trailing _word segments (e.g. gamename_publisher_disk1)
    parts = stem.split('_')
    while len(parts) > 1:
        parts.pop()
        s_strip = '_'.join(parts)
        if (c := _try(s_strip)):
            yield c
            # also try with spaces
            s_strip_sp = _UNDERSCORE_RE.sub(' ', s_strip)
            if (c2 := _try(s_strip_sp)):
                yield c2

    # Step 4: replace remaining underscores with spaces
    s4 = _UNDERSCORE_RE.sub(' ', stem)
    if (c := _try(s4)):
        yield c

