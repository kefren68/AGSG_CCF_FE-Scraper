def build_screenscraper_system_ids(devid=None, devpassword=None):
    """
    Fetch the complete system list from ScreenScraper (245+ systems) and
    return a name → system_id mapping built from all available name fields.
    Result is cached globally so the API is called only once per session.
    Falls back to empty dict on network failure; caller merges with SYSTEM_IDS.
    """
    global _dynamic_system_ids_cache
    if _dynamic_system_ids_cache is not None:
        return _dynamic_system_ids_cache

    _devid  = devid  or DEFAULT_DEVID
    _devpw  = devpassword or DEFAULT_DEVPASSWORD

    print("Fetching system list from ScreenScraper...", end=" ", flush=True)
    try:
        params = {
            "devid":       _devid,
            "devpassword": _devpw,
            "softname":    SOFT_NAME,
            "output":      "json",
        }
        r = requests.get(f"{API_BASE}/systemesListe.php",
                         params=params, timeout=REQUEST_TIMEOUT)
        r.raise_for_status()
        systems = r.json().get("response", {}).get("systemes", [])
    except Exception as e:
        print(f"failed ({e}). Using built-in list only.")
        _dynamic_system_ids_cache = {}
        return {}

    mapping = {}
    for s in systems:
        try:
            sid = int(s.get("id", 0))
        except (ValueError, TypeError):
            continue
        if not sid:
            continue
        noms = s.get("noms", {})
        for field in ("nom_eu", "nom_us", "nom_jp", "nom_recalbox",
                      "nom_retropie", "nom_launchbox", "nom_hyperspin", "noms_commun"):
            for part in noms.get(field, "").split(","):
                part = part.strip().lower()
                if part and part not in mapping:
                    mapping[part] = sid

    print(f"{len(systems)} systems loaded ({len(mapping)} name aliases).")
    _dynamic_system_ids_cache = mapping
    return mapping
