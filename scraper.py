def md5_of_file(path):
    """Return MD5 hex digest of a file."""
    h = hashlib.md5()
    with open(path, "rb") as f:
        while True:
            chunk = f.read(65536)
            if not chunk:
                break
            h.update(chunk)
    return h.hexdigest()


def pick_region(items, tag_key, preferred, text_key="text"):
    """
    Pick the best value from a list of region/language-tagged dicts.
    Tries preferred → 'wor' → first available.
    """
    # ScreenScraper's PHP API sometimes returns a single item as a dict instead of a list
    if isinstance(items, dict):
        items = [items]
    if not items:
        return None
    for item in items:
        if item.get(tag_key, "").lower() == preferred.lower():
            return item.get(text_key)
    for item in items:
        if item.get(tag_key, "").lower() == "wor":
            return item.get(text_key)
    return items[0].get(text_key)


