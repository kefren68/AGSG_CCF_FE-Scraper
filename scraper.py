    def rating(self, jeu):
        """Return rating as a 0.0–1.0 string (EmulationStation format); source is /20."""
        note = jeu.get("note")
        if isinstance(note, dict):
            note = note.get("text")
        if note is None:
            return None
        try:
            return f"{float(note) / 20:.2f}"
        except (ValueError, TypeError):
            return None

    def all_genres(self, jeu):
        """Return all genres as a comma-separated string."""
        genres = jeu.get("genres", [])
        if isinstance(genres, dict):
            genres = [genres]
        names = []
        for g in genres:
            name = pick_region(g.get("noms", []), "langue", self.lang)
            if name and name not in names:
                names.append(name)
        return ", ".join(names) if names else None

    def media_url(self, jeu, media_type):
        """Return the best URL for a given media type (boxart, video, etc.)."""
        medias     = jeu.get("medias", [])
        candidates = [m for m in medias if m.get("type") == media_type]
        if not candidates:
            return None
        for reg in (self.region, "wor", "us", "eu", "jp", ""):
            for m in candidates:
                if m.get("region", "").lower() == reg.lower():
                    return m.get("url")
        return candidates[0].get("url")

    def authenticated_url(self, url):
        """Re-inject authentication parameters into a cached (stripped) URL."""
        if not url:
            return url
        parsed = urlparse(url)
        params = parse_qs(parsed.query, keep_blank_values=True)
        params.update({
            "devid":       [self.devid],
            "devpassword": [self.devpassword],
            "softname":    [SOFT_NAME],
            "ssid":        [self.user],
            "sspassword":  [self.password],
        })
        return urlunparse(parsed._replace(query=urlencode(
            {k: v[0] for k, v in params.items()}
        )))


