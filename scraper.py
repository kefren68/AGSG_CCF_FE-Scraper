class SFTPContext:
    """
    Thin wrapper around paramiko SFTP for reading ROM lists from a remote
    device and writing scraper outputs (boxart, videos, gamelist.xml …) back.
    """

    def __init__(self, host, port, username, password=None, key_filename=None,
                 passphrase=None):
        import paramiko  # type: ignore  (optional dependency)

        # Expand ~ in an explicit key path
        if key_filename:
            key_filename = os.path.expanduser(key_filename)

        # If no key given, consult ~/.ssh/config (like the ssh CLI does)
        if not key_filename:
            try:
                _cfg = paramiko.SSHConfig()
                _cfg_path = os.path.expanduser("~/.ssh/config")
                if os.path.exists(_cfg_path):
                    with open(_cfg_path) as _f:
                        _cfg.parse(_f)
                _h = _cfg.lookup(host)
                if _h.get("identityfile"):
                    key_filename = os.path.expanduser(_h["identityfile"][0])
            except Exception:
                pass

        self._client = paramiko.SSHClient()
        self._client.set_missing_host_key_policy(paramiko.AutoAddPolicy())
        self._client.connect(
            host, port=int(port), username=username,
            password=password or None,
            key_filename=key_filename or None,
            passphrase=passphrase or None,
            timeout=15,
        )
        self._sftp = self._client.open_sftp()

    def close(self):
        try:
            self._sftp.close()
            self._client.close()
        except Exception:
            pass

    def exists(self, remote_path):
        try:
            self._sftp.stat(remote_path)
            return True
        except FileNotFoundError:
            return False

    def makedirs(self, remote_path):
        """Recursive mkdir for a remote path (like mkdir -p)."""
        import posixpath
        parts = remote_path.replace("\\", "/").split("/")
        current = ""
        for part in parts:
            if not part:
                current = "/"
                continue
            current = current.rstrip("/") + "/" + part
            try:
                self._sftp.stat(current)
            except FileNotFoundError:
                try:
                    self._sftp.mkdir(current)
                except OSError:
                    pass  # may already exist

    def put(self, local_path, remote_path):
        """Upload a local file to the remote path."""
        self.makedirs(str(Path(remote_path).parent).replace("\\", "/"))
        self._sftp.put(str(local_path), remote_path)

    def get(self, remote_path, local_path):
        """Download a remote file to a local path."""
        Path(local_path).parent.mkdir(parents=True, exist_ok=True)
        self._sftp.get(remote_path, str(local_path))

    def list_subdirs(self, remote_path):
        """Return names of immediate non-hidden subdirectories."""
        import stat as _st
        try:
            attrs = self._sftp.listdir_attr(remote_path)
        except FileNotFoundError:
            return []
        return [a.filename for a in attrs
                if _st.S_ISDIR(a.st_mode or 0) and not a.filename.startswith(".")]

    def list_files_recursive(self, remote_path):
        """
        Return list of (remote_file_path, size, filename) for all files
        under remote_path, recursively.
        """
        import stat as _st
        results = []
        try:
            attrs = self._sftp.listdir_attr(remote_path)
        except FileNotFoundError:
            return []
        for a in attrs:
            full = remote_path.rstrip("/") + "/" + a.filename
            if _st.S_ISDIR(a.st_mode or 0):
                if not a.filename.startswith("."):
                    results.extend(self.list_files_recursive(full))
            else:
                results.append((full, a.st_size or 0, a.filename))
        return results

    def list_files_in_dir(self, remote_path):
        """Return list of filenames (non-recursive) in a remote directory."""
        import stat as _st
        try:
            attrs = self._sftp.listdir_attr(remote_path)
        except FileNotFoundError:
            return []
        return [a.filename for a in attrs if not _st.S_ISDIR(a.st_mode or 0)]


