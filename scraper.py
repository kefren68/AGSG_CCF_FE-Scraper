class ScreenScraperClient:
    def __init__(self, user, password, devid=None, devpassword=None,
                 region="eu", lang="en", delay=DEFAULT_DELAY, verbose=False):
        self.user        = user
        self.password    = password
        self.devid       = devid or DEFAULT_DEVID
        self.devpassword = devpassword or DEFAULT_DEVPASSWORD
        self.region      = region
        self.lang        = lang
        self.delay       = delay
        self.verbose     = verbose
        self._last_call  = 0.0
        self.session     = requests.Session()
        self.session.headers.update({"User-Agent": f"{SOFT_NAME}/1.0"})
        # Quota tracking (populated from API responses)
        self.requests_today   = None   # type: int | None
        self.max_requests_day = None   # type: int | None
        self.ko_today         = None   # type: int | None  (ROM not found = KO)
        self.max_ko_day       = None   # type: int | None
        # Set to True when a 431 is received: blocks all further API calls
        self.quota_exceeded   = False
        # optional callback(today, max_day, ko_today, max_ko_day)
        self.on_quota_update  = None

    def _throttle(self):
        elapsed = time.time() - self._last_call
        if elapsed < self.delay:
            time.sleep(self.delay - elapsed)
        self._last_call = time.time()

    def _parse_quota(self, data):
        """Extract and store quota info from an API response's ssuser block."""
        ssuser = data.get("response", {}).get("ssuser", {})
        if not ssuser:
            return
        try:
            today      = int(ssuser.get("requeststoday")    or 0)
            max_day    = int(ssuser.get("maxrequestsperday") or 0)
            ko_today   = int(ssuser.get("koscrapestoday")    or 0)
            max_ko_day = int(ssuser.get("maxkoscrapesperday") or 0)
        except (ValueError, TypeError):
            return
        if max_day > 0:
            self.requests_today   = today
            self.max_requests_day = max_day
        if max_ko_day > 0:
            self.ko_today   = ko_today
            self.max_ko_day = max_ko_day
        if self.on_quota_update and (max_day > 0 or max_ko_day > 0):
            self.on_quota_update(
                self.requests_today, self.max_requests_day,
                self.ko_today, self.max_ko_day,
            )

    def quota_str(self):
        """Return a human-readable quota string, or empty string if unknown."""
        parts = []
        if self.requests_today is not None and self.max_requests_day:
            rem = self.max_requests_day - self.requests_today
            parts.append(
                f"Scrape: {self.requests_today}/{self.max_requests_day} ({rem} left)"
            )
        if self.ko_today is not None and self.max_ko_day:
            rem_ko = self.max_ko_day - self.ko_today
            parts.append(
                f"KO: {self.ko_today}/{self.max_ko_day} ({rem_ko} left)"
            )
        return "  |  ".join(parts) if parts else ""

    def _base_params(self):
        p = {
            "devid":      self.devid,
            "devpassword": self.devpassword,
            "softname":   SOFT_NAME,
            "output":     "json",
            "ssid":       self.user,
            "sspassword": self.password,
        }
        return p

    def get_user_quota(self):
        """
        Call ssUserInfos.php to retrieve the current quota before scraping starts.
        Populates self.requests_today / self.max_requests_day and fires the callback.
        Returns True on success, False on network failure.
        """
        try:
            r = self.session.get(
                f"{API_BASE}/ssUserInfos.php",
                params=self._base_params(),
                timeout=REQUEST_TIMEOUT,
            )
            r.raise_for_status()
            self._parse_quota(r.json())
            return True
        except requests.HTTPError as e:
            code = e.response.status_code if e.response is not None else 0
            if code == 431:
                self._parse_quota_from_error(e.response)
            return False
        except Exception:
            return False

    def _parse_quota_from_error(self, response):
        """Try to extract ssuser quota info from a non-200 response body."""
        try:
            data = response.json()
            self._parse_quota(data)
        except Exception:
            pass

    def get_game_info(self, system_id, rom_name, rom_path=None, file_size=None):
        """
        Query ScreenScraper for a single ROM. Returns the 'jeu' dict or None.
        Sends MD5 hash when available for more accurate matching.
        If not found, retries with progressively simplified filenames.
        file_size: pre-computed remote file size (SFTP mode); skips local hash.
        """
        if self.quota_exceeded:
            return None

        jeu = self._query_game_info(system_id, rom_name, rom_path, file_size=file_size)
        if jeu is not None:
            return jeu

        # Progressive fallback: strip tags, WHDLoad suffixes, underscores
        for candidate in fallback_names(rom_name):
            if self.quota_exceeded:
                return None
            if self.verbose:
                print(f"\n      [verbose] not found, retrying as '{candidate}'",
                      file=sys.stderr)
            jeu = self._query_game_info(system_id, candidate, rom_path=None)
            if jeu is not None:
                if self.verbose:
                    print(f"      [verbose] found via '{candidate}'", file=sys.stderr)
                return jeu
        return None

    def _query_game_info(self, system_id, rom_name, rom_path=None, file_size=None):
        """Single API call to jeuInfos.php. Returns 'jeu' dict or None."""
        if self.quota_exceeded:
            return None
        self._throttle()
        params = self._base_params()
        params["systemeid"] = system_id
        params["romnom"]    = rom_name

        if file_size is not None:
            # SFTP mode: we know the size but can't compute the MD5 locally
            params["romtaille"] = file_size
        elif rom_path and rom_path.exists():
            size = rom_path.stat().st_size
            if size < MAX_MD5_SIZE:
                try:
                    params["rommd5"]    = md5_of_file(rom_path)
                    params["romtaille"] = size
                except OSError:
                    pass

        if self.verbose:
            md5_str = params.get("rommd5", "(none)")
            print(f"\n      [verbose] GET jeuInfos  system={system_id}  rom='{rom_name}'  md5={md5_str}",
                  file=sys.stderr)
        try:
            r = self.session.get(f"{API_BASE}/jeuInfos.php",
                                 params=params, timeout=REQUEST_TIMEOUT)
            r.raise_for_status()
            data = r.json()
            self._parse_quota(data)
            jeu = data.get("response", {}).get("jeu")
            if self.verbose and jeu:
                found_name = jeu.get("noms", [{}])[0].get("text", "?") if jeu.get("noms") else "?"
                print(f"      [verbose] matched: '{found_name}'", file=sys.stderr)
            return jeu
        except requests.HTTPError as e:
            code = e.response.status_code if e.response is not None else 0
            if code == 404:
                if self.verbose:
                    print(f"      [verbose] 404 not found", file=sys.stderr)
                return None  # game not in DB, not an error
            if code == 403:
                # Show the actual ScreenScraper error message for diagnosis
                body = ""
                try:
                    body = e.response.text[:300]
                except Exception:
                    pass
                print(
                    f"\n    [!] 403 Forbidden for '{rom_name}'.\n"
                    f"        → Server response: {body}",
                    file=sys.stderr,
                )
                return None
            if code == 429:
                print(f"\n    [!] Rate limit — try increasing --delay.", file=sys.stderr)
                return None
            if code == 431:
                self._parse_quota_from_error(e.response)
                self.quota_exceeded = True
                qs = self.quota_str()
                print(
                    f"\n    [!] 431 Quota giornaliera esaurita per '{rom_name}'."
                    + (f"\n        → {qs}" if qs else "")
                    + "\n    [!] Nessun'altra chiamata API verrà effettuata per oggi.",
                    file=sys.stderr,
                )
                return None
            safe = e.response.url.split("?")[0] if e.response is not None else ""
            print(f"\n    [!] HTTP {code} for '{rom_name}' ({safe})", file=sys.stderr)
            return None
        except (requests.RequestException, json.JSONDecodeError) as e:
            print(f"\n    [!] API error for '{rom_name}': {type(e).__name__}: {e}",
                  file=sys.stderr)
            return None

    # Map Content-Type → file extension for media downloads
    _CONTENT_TYPE_EXT = {
        "application/pdf":  ".pdf",
        "image/jpeg":       ".jpg",
        "image/jpg":        ".jpg",
        "image/png":        ".png",
        "image/gif":        ".gif",
        "image/webp":       ".webp",
        "video/mp4":        ".mp4",
        "video/webm":       ".webm",
    }

    def download_file(self, url, dest_path):
        """Download url to dest_path. Returns True on success."""
        try:
            r = self.session.get(url, timeout=REQUEST_TIMEOUT, stream=True)
            r.raise_for_status()
            dest_path.parent.mkdir(parents=True, exist_ok=True)
            with open(dest_path, "wb") as f:
                for chunk in r.iter_content(65536):
                    f.write(chunk)
            return True
        except requests.RequestException as e:
            print(f"\n    [!] Download failed: {e}", file=sys.stderr)
            return False

    def download_file_detect_ext(self, url, stem_path):
        """
        Download url to stem_path.<detected_ext>.
        Extension is determined from the Content-Type response header.
        Returns the actual Path used on success, or None on failure.
        """
        try:
            r = self.session.get(url, timeout=REQUEST_TIMEOUT, stream=True)
            r.raise_for_status()
            ct = r.headers.get("Content-Type", "").split(";")[0].strip().lower()
            ext = self._CONTENT_TYPE_EXT.get(ct)
            if not ext:
                # Last-resort: try URL path suffix, but ignore .php
                url_suffix = Path(urlparse(url).path).suffix.lower()
                ext = url_suffix if url_suffix and url_suffix != ".php" else ".pdf"
            dest_path = Path(str(stem_path) + ext)
            dest_path.parent.mkdir(parents=True, exist_ok=True)
            with open(dest_path, "wb") as f:
                for chunk in r.iter_content(65536):
                    f.write(chunk)
            return dest_path
        except requests.RequestException as e:
            print(f"\n    [!] Download failed: {e}", file=sys.stderr)
            return None

